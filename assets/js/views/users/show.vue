<template>
  <main>
    <hero-title
      v-if='user'
      :text="user.profile.name"
      :subtitle="`@${user.username}`"
    />

    <hero-title
      v-if="status === 'errored'"
      text="User does not exist"
      color="danger"
    />

    <div v-if='user' class="container">
      <div class="columns">
        <div class="column is-one-quarter">
          <div class="image">
            <img :src="gravatar(user.email)" alt="Avatar"/>
          </div>

          <p v-if="user.profile.bio">{{user.profile.bio}}</p>

          <div v-if="userInfos" class="panel">
            <p v-for="info in userInfos" class="panel-block">
              <span class="panel-icon">
                <i class="fa" :class="userInfoIconClasses[info.key]" />
              </span>
              {{info.text}}
            </p>
          </div>

          <router-link
            v-if="currentUserIsSelf"
            :to="{name: 'userEdit'}"
            class="button is-info is-outlined is-fullwidth"
          >
            <span class="icon is-small">
              <i class="fa fa-cog"></i>
            </span>

            <span>Edit profile</span>
          </router-link>
        </div>

        <div class="column">
          <article v-for="org in organizations" class="media">
            <div class="media-content">
              <div class="content">
                <div class="icon is-small">
                  <i class="fa fa-group" />&nbsp;
                </div>
                <strong>
                  <router-link
                  :to="{name: 'organizationShow', params: {organization: org.name}}"
                  >
                  {{org.displayName || org.name}}
                </strong>
                <p>{{org.description}}</p>
              </div>

              <article v-for="proj in projects" class="media">
                <!-- This empty picture pulls the nested content right -->
                <figure class="media-left"><p class="image is-48x48"></p></figure>

                <div class="media-content">
                  <div class="icon is-small">
                    <i class="fa fa-list-alt" />&nbsp;
                  </div>
                  <strong>{{proj.displayName}}</strong>
                  <p>{{proj.description}}</p>
                </div>
              </article>
            </div>
          </article>
        </div>
      </div>
    </div>
  </main>
</template>

<script>
  import {mapState} from 'vuex'
  import R from 'ramda'
  import gravatar from 'gravatar'
  import {Users, Organizations} from 'app/api'
  import {HeroTitle} from 'app/components'

  export default {
    name: 'UserShowView',

    components: {HeroTitle},

    data() {
      return {
        status: {
          users: 'not-asked',
          organization: 'not-asked',
          project: 'not-asked'
        },

        user: null,
        organizations: [],
        projects: [],

        userInfoIconClasses: {
          location: {'fa-map-marker': true},
          email: {'fa-envelope': true},
          url: {'fa-globe': true}
        }
      }
    },

    methods: {
      gravatar(email) {
        return gravatar.url(email, {size: 512})
      }
    },

    computed: {
      ...mapState({
        loggedinId: R.view(R.lensPath(['auth', 'user', 'id']))
      }),

      currentUserIsSelf() {
        return this.loggedinId === this.user.id
      },

      userInfos() {
        const profile = R.view(R.lensPath(['user', 'profile']))(this)

        return R.pipe(
          R.prop('user'),
          R.pick(['email']),
          R.merge(
            R.pick(['location', 'url'], profile)
          ),
          R.filter(Boolean),
          R.toPairs,
          R.map(([key, text]) => ({text, key})),
        )(this)
      }
    },

    async created() {
      this.status.organization = 'loading'

      const {data} = await Organizations.all()

      if (data.length === 0) {
        this.status = 'errored'
        return
      }

      this.status.organization = 'success'

      this.organizations = data

      const org = data[0]

      this.status.project = 'loading'

      const proj = await Organizations.projects.all(org.id)

      if (proj.data.length === 0) {
        this.status.project = 'errored'
        return
      }

      this.projects = proj.data

      const res = await Users.show(this.$route.params.username)

      if (res.data.length === 0) {
        this.status = 'errored'
        return
      }

      this.status = 'success'

      this.user = res.data[0]
    }
}
</script>
